package com.example.android.synapse;

public class Channel
{
    String name;
    String id;

    public Channel()
    {

    }

    public Channel(String name, String id) {
        this.name = name;
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

}